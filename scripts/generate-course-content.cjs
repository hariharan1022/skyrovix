const fs = require('fs');
const path = require('path');

const CONTENT_DIR = path.resolve(__dirname, '..', 'course-content');
const OUTPUT = path.resolve(__dirname, '..', 'src', 'lib', 'course-content.generated.ts');

const DIR_TO_SLUG = {
  python: 'python', java: 'java', html: 'html', css: 'css',
  javascript: 'javascript', php: 'php', mysql: 'mysql',
  django: 'django', numpy: 'numpy', pandas: 'pandas',
  scipy: 'scipy', matplotlib: 'matplotlib',
};

const allEntries = [];

for (const [dirName, slug] of Object.entries(DIR_TO_SLUG)) {
  const dir = path.join(CONTENT_DIR, dirName);
  if (!fs.existsSync(dir)) { console.warn(`SKIP: ${dirName} not found`); continue; }
  const files = fs.readdirSync(dir).filter(f => f.endsWith('.md')).sort();
  for (const file of files) {
    const raw = fs.readFileSync(path.join(dir, file), 'utf8');
    const num = parseInt(file.match(/^(\d+)/)?.[1] ?? '0');
    const title = raw.match(/^## \d+\.\s*(.+)$/m)?.[1]?.trim() ?? file.replace('.md', '').replace(/^\d+_/, '').replace(/_/g, ' ');
    const codeMatch = raw.match(/```[\w]*\n([\s\S]*?)```/);
    const code_example = codeMatch?.[1]?.trim() ?? null;
    allEntries.push({ slug, order_index: num, title, content_md: raw, code_example });
  }
}

const code = `// Auto-generated by scripts/generate-course-content.cjs - DO NOT EDIT
// Run: node scripts/generate-course-content.cjs

export type CourseTopicEntry = {
  slug: string;
  order_index: number;
  title: string;
  content_md: string;
  code_example: string | null;
};

const _data: CourseTopicEntry[] = ${JSON.stringify(allEntries)};

const _bySlug: Record<string, CourseTopicEntry[]> = {};
for (const e of _data) {
  if (!_bySlug[e.slug]) _bySlug[e.slug] = [];
  _bySlug[e.slug].push(e);
}
for (const key of Object.keys(_bySlug)) {
  _bySlug[key].sort((a, b) => a.order_index - b.order_index);
}

export function getLocalCourseContent(slug: string): CourseTopicEntry[] {
  return _bySlug[slug] ?? [];
}

export function getLocalCourseSlugs(): string[] {
  return Object.keys(_bySlug).sort();
}

export function getLocalTopicCount(slug: string): number {
  return _bySlug[slug]?.length ?? 0;
}
`;

fs.writeFileSync(OUTPUT, code, 'utf8');
console.log(`Generated ${OUTPUT} with ${allEntries.length} entries`);
