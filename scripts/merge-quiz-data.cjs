const fs = require('fs');
const path = require('path');

const files = [
  'quiz-python-html-css.json',
  'quiz-java-js-php.json',
  'quiz-mysql-django-numpy.json',
  'quiz-pandas-scipy-matplotlib.json',
];

let all = [];
for (const f of files) {
  const fp = path.join(__dirname, f);
  if (!fs.existsSync(fp)) { console.error(`MISSING: ${f}`); continue; }
  const data = JSON.parse(fs.readFileSync(fp, 'utf8'));
  all = all.concat(data);
  console.log(`${f}: ${data.length} questions`);
}

console.log(`\nTotal: ${all.length} questions`);

const bySlug = {};
for (const q of all) {
  const slug = q.id.split('-')[0];
  if (!bySlug[slug]) bySlug[slug] = [];
  bySlug[slug].push(q);
}
for (const slug of Object.keys(bySlug)) {
  bySlug[slug].sort((a, b) => parseInt(a.id.split('-')[1]) - parseInt(b.id.split('-')[1]));
  console.log(`${slug}: ${bySlug[slug].length} questions`);
}

const outputPath = path.join(__dirname, '..', 'src', 'lib', 'quiz-content.generated.ts');
const code = `// Auto-generated by scripts/merge-quiz-data.cjs
export type QuizQuestion = {
  id: string;
  question: string;
  options: string[];
  correct_index: number;
  marks: number;
  explanation: string;
};

const _data: QuizQuestion[] = ${JSON.stringify(all)};

const _bySlug: Record<string, QuizQuestion[]> = {};
for (const q of _data) {
  const slug = q.id.split('-')[0];
  if (!_bySlug[slug]) _bySlug[slug] = [];
  _bySlug[slug].push(q);
}

export function getLocalQuizQuestions(slug: string): QuizQuestion[] {
  return _bySlug[slug] ?? [];
}

export function getLocalQuizCount(slug: string): number {
  return _bySlug[slug]?.length ?? 0;
}
`;

fs.writeFileSync(outputPath, code, 'utf8');
console.log(`\nGenerated: ${outputPath}`);
